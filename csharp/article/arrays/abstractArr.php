<h2>Datové struktury</h2>
<p class="introduction">Data jsou vždy uložena dle jednotného řádu - struktury. Různé typy struktur umožňují velkou variabilitu při řešení různých problémů.</p>
<p>Datový typ definuje druh hodnot, kterých může nabývat proměnná (čísla, znaky..)<br>
Abstrakce je proces zjednodušování složitých objektů na jednodušší např. obrázek reálného auta => siluleta auta<br>
Rekurze je opakované využívání sama sebe</p>
<h3>Datový typ vs. datová struktura</h3>
<p><strong>Datový typ je základem datových struktur.</strong><br>
Datovou strukturu lze charakterizovat jako přístupové rozhraní k datovému typu, přidává k němu i několik dalších metod.<br>
Konkrétním příkladem datové struktury je třída String (v .NET je implementován třídou), která rozvíjí char a přidává k němu další metody.
</p><br>Základní datové struktury:
<ol>
	<li>Pole</li>
	<li>Množina</li>
	<li>Fronta</li>
	<li>Zásobník</li>
	<li>Hashovací tabulka</li>
	<li>List - asociativní pole</li>
	<li>Strom</li>
	<li>Binární strom</li>
</ol><br><br>


<h3>Dělení datových struktur</h3>
Datové struktury lze dělit na základní a abstraktní (odvozené)<br>
<ol>
	<li>Základní - nemohou měnit svůj rozsah</li>
	<li>Odvozené - mohou měnit svůj rozsah</li>
</ol><br>
Základní datové struktury
<ul>
	<li>Proměnná</li>
	<li>Pole</li>
	<li>Object</li>
