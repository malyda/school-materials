<h2>Množina - set</h2>
<p class="introduction">Datová struktura, která ukládá data bez ohledu na pořadí jejich umístění.</p>

<p>Z hlediska paměťové náročnosti se jedná o nejprimitivnější datovou strukturu.<br>
Je implementací matematického termínu množina, kde <strong>každý prvek může být množinou obsažen maximálně jednou</strong>.<br>
Jednoduše řečeno se jedná o pole bez indexů se zamezením opakujících se hodnot.<br>
Speciálním typem množiny je multimnožina, kde se data mohou opakovat.<br>
V C# je implementována pomocí datové struktury HashSet (hashovací tabulka).</p>
