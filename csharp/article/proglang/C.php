<H2> C-like jazyky </H2>
<p class="introduction">Programovací jazyky na základě C -> C-like.<br>
Vznikly pro potřeby co nejjednoduššího vývoje složitějších systémů. Jazyky, které byly do roku 1970 dostupné, nedokázali zajistit potřebnou efektivitu vývoje při programování složitějších celků. Způsobem používání byly blízko počítači, tedy náročnější pro programátory a vývoj trval dlouho.
</p>
<H3> C </H3>
<ul>
	<li> Tvůrce: <b>Denis Ritchie </b></li>
	<li> Produkt vytvořený pomocí jazyka C: <b> OS UNIX </b></li>
	<li> Rok: <b> 1970 </b> - vynalezení jazyka C pro potřeby vývoje OS UNIX.</li>
	<li> Příbuzné jazyky: C++, Objective-C, C# </li>
	<li> <b> Procedurální </b> - dělí program na podprogramy, funkce </li>
	<li> <b> Strukturovaný </b> - kód je rozdělen na bloky - {}, jednotlivé příkazy jsou oddělené - ; </li>	
</ul>
<H3> C++ </H3>

<p>Pozn.<I> ++ je to samé jako +1 -> C+1.</I></p>
<p>Jde o další verzi jazyka C, která přinesla tolik změn, že bylo nutné novou verzi od staré odlišit -> C++.</p>
<p>Rozdíly mezi C a C++ jsou např. :</p>
<p>C++</p><ul>
	<li> Umožňuje <b>objektové programování</b>, podporuje i procedurální </li> 
	<li> Mnoho nových knihoven a nástrojů - bližší k programátorovi</li>
	<li> Mnoho norem, kde se například <b> mezi normou C99 a C11 nedodržela zpětná kompatibilita </b></li>
</ul>
	<p>C</p>
<ul>
	<li> Není tak robustní </li>
	<li> V současnosti se používá méně než C++ </li>
	<li> Méně nástrojů a knihoven -> DIY (udělej si sám) </li><br>
	<p>Pozn.<i> C se používá  v případech, kdy je potřeba přenositelný Assembler.</i></p>
</ul>
<H3> Java </H3>
<ul>
	<li> Vytvořen: 1994 </li>
	<li> <strong>Objektový - dělí program na třídy a metody </strong>
	<br> Pozn. <i> umožňuje procedurální přístup, ale jde vždy o pokus psát v objektovém jazyce neobjektově </i> </li>	
</ul>
