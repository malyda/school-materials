<H2>Programovací paradigma </H2>
<p class="introduction">Paradigma určuje <strong>nástroje</strong> pomocí, kterých lze docílit výsledku.<br>
Metodika vymezuje <strong>způsob</strong>,kterým lze výsledku docílit.</p>
<p>Příklad: Cesta do Paříže:<br>
Paradigma jako nástroj sloužící k cestování -> auto, letadlo<br>
Metodika jako plán cesty -> koupit letenky, nastoupit do letadla atd.</p>

<H3>Paradigma programovacích jazyků</H3>

<p>Vymezují, jak musí programátor vyvíjet aplikaci</p>
	<ul>
		<li>SmallTalk -> <strong>pouze objektový</strong></li>
		<li>Zakázání konkrétních příkazů - goto</li>
		<li>Zamezení vedlejších účinků -> situace, kdy metoda, či funkce pracuje s externími zdroji, které ji nepodléhají např. paměť jiné aplikace</li>
	</ul>

<p>Příklady zjednodušení:</p><ul>
	<li>Třída String použita jako implementovaný datový typ</li>
	<li>Zjednodušený <strong>for -> foreach</strong> pro čtení dat</li>
</ul>
<h3>Datové typy</h3>
<p>C-like jazyky jsou <strong>striktně</strong> orientované na datové typy. To znamená, že <strong>každá proměnná je právě jednoho datového typu</strong> a všechny akce s ní podléhají tomuto typu -> je možné ji přetypovat.<br>
Existují dvě skupiny programátorů:</p>
<ul>
	<li>Ti, kteří preferují, <strong>co největší volnost</strong> -> PHP, Python, Javascript</li>
	<li>Ti, kteří preferují jasná <strong>pravidla, na která se mohou vždy spolehnout</strong> -> C-like jazyky</li>
</ul>