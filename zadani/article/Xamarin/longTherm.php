<h2>Vlastní téma - Mobilní aplikace</h2>
<p class="introduction">
    Vytvořte vlastní mobilní aplikaci pomocí multiplatformního frameworku. O schválení tématu aplikace je rozhodnuto na základě design dokumentu.
</p>
<h3>Návrh aplikace - design dokument</h3>
<p>
    Vytvořte <a href="https://www.toptal.com/freelance/why-design-documents-matter">design dokument</a> aplikace, což je celkový popis aplikace.</p>
<p>
    Součástí tohoto dokumentu je UI návrh aplikace vypracovaný např. v Marvelapp, nebo Adobe Experience.
</p>
<p>Základní osnova design dokumentu</p>
<ul>
    <li>Funkcionalita aplikace</li>
    <li>UI</li>
    <li>Dílčí cíle a termíny (každý týden jeden cíl)</li>
</ul>
<h4>Až na základě design dokumentu je téma práce schváleno.</h4>


<h3>
	Povinná funkcionalita aplikace
</h3>
	<ul>
		<li>Ukládání, načítání a zobrazování dat z lokálního úložiště zařízení (databáze, nebo vlastní soubory)</li>
		<li>Funkční na Androidu</li>
	</ul>
	Povinně použité grafické elementy:
	<ul>
		<li>min.4 Page (nepočítá se načítací)</li>
		<li>Správné použití Master detail Page, Navigation Page (vč. Navigation bar) nebo Carousel Page (či ekvivalentní dle frameworku)</li>
		<li>Většina grafiky ja psaná v XAML nebo v jiném značkovacím jazyku (dle frameworku)</li>
	</ul>
	Požadavky na kód:
	<ul>
		<li>Kód je rozdělen do tříd a metod. Jako třídy se nepočítají jednotlivé Page.</li>
		<li>Struktura je jasně rozdělená na: UI a logiku.</li>
		<li>Class diagram je součástí odevzdání projektu</li>
	</ul>
	Bonusy za:
	<ul>
		<li>Asynchronní programování</li>
		<li>Notifikace a práce s procesy na pozadí</li>
		<li>Možnost nastavení lokalizace aplikace (alespoň 2 jazyky)- String resources</li>
		<li>atd.</li>
	</ul>
	<p>
		Požadavky se mohou lišit individuálně dle typu aplikace, po zkonzultování.
	</p>
	<p>Příklady aplikací, ze kterých můžete čerpat, pokud Vás nenapadne vlastní téma</p>
	<ul>
		<li>Hra pro cvičení paměti, postřehu atd.</li>
		<li>Úkolník, poznámky</li>
		<li>Sportovní statistiky</li>
        <li>Aplikace upozorňující na konec platnosti povinného vybavení ve vozidle - lékárnička, technická atd.</li>
        <li>Hodnocení projektů na základě vah</li>
	</ul>
