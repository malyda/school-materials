<h2>Cvičení pro zopakování látky</h2>
<h3>Zadání pro opakování práce s polem</h3>
<p>Napište programy, které:</p>
<ol>
	<li>Vypíše do konzole obdélník 17x13</li>
	<li>Vypočte průměrnou hodnotu v poli</li>
	<li>Najde specifický řetězec v poli</li>
	<li>Zadané pole (string) převede na pole čísel (int)</li>
	<li>Najde minimální a maximální hodnotu v poli</li>
</ol>

<h3>Metody a funkce</h3>
<p>Napište programy, které:</p>
<ol>
	<li>Používají sadu metod pro ošetření uživ. vstupů. Ošetřují, jestli je zadaný text číslo s požadovanou délkou, rodné číslo a email. Vstupem je vždy string.</li>
	<li>Vypíše zadané číslo (decimálně) v binární podobě</li>
	<li>Používají metody pro výpis všech čísel v daném rozsahu (int min, int max) a jsou zároveň dělitelná oběma vstupy funkce</li>
</ol>