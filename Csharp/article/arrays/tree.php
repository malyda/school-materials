<h2>Strom</h2>
<p class="introduction">Hierarchická struktura, kde každý rodič má 0 až x potomků, každý potomek má právě jednoho rodiče.</p>
<p>Rekurzivní vlastností stromu je, že každý potomek, může být zároveň rodič ve vlastním stromu.<br>
V OS Unix je hlavní rodič označován jako root. Stromová struktura je k nalezení v sítích (hierarchická topologie) či v operačních systémech (hierarchická struktura složek).</p>

